public class Data_Types{
    public static void main(String[] args)
    {
        char a = 'A'; // 0 to 255 ASCII Value
        byte b = 4;  // range -127 to 128
        int number = 123; // range -2,147,483,648 to 2,147,483,647
        short s = 56; //range -32,768 to 32,767
        long l = 123456789; //range -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
        float f = 3.789f; //range upto 7 decimal places;
        double d = 2.7890654; // range upto 16 decimal places;
        boolean x = true;
        System.out.println("Character is : " + a);
        System.out.println("Byte is : " + b);
        System.out.println("Int is : " + number);
        System.out.println("Short is : " + s);
        System.out.println("float is : " + f);
        System.out.println("long is : " + l);
        System.out.println("double is : " + d);
        System.out.println("boolean is : " + x);

    }
}
